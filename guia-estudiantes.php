        <h2><strong>GUÍA PARA ESTUDIANTES</strong></h2>
        <span class='byline'>
            1. Cada estudiante deberá cumplir todas las reglas del centro educativo. <br/>

2. Al ingresar, cualquier detalle significante con respecto al estudiante, como condición 

Médica e.T.C., deberá ser presentada. Un reporte escolar deberá ser producido. Solo 

Estudiantes que estén sinceramente interesados en estudiar por su propio deseo se 

Les dará un lugar, un guardián/padre entusiasta no se considerará suficiente en la 

Admisión. <br/>

3. Los interesados deberán poder leer o recitar el quran-kareem de forma fluida y deberán 

Haber completado el estudio basico (deeniyat). Los que apliquen no deberán ser menores 

De 12 años. <br/>

4. La puntualidad en el rezo en congregación es de mucha importancia para cada 

Estudiante. No se aceptará ninguna excusa no islámica por faltar al jamaat (salaat). 

Dejadez intencional de rezos será tomado con prioridad, posiblemente resultando en 

Expulsión. Este procedimiento se aplica a todos los actos no islamicos. <br/>

5. El comportamiento de cada estudiante, carácter y presentacion, código de vestido y 

Apariencia deberá parecerse a los devotos mayores del ummah. <br/>

6. Fumar, estilos de cabello no islamicos, afeitarse o cortarse la barba están 

Estrictamente prohibidos. Pelear o argumentar con compañeros estudiantes o 

Miembros del personal (cocinero, supervisor, limpiador, etc.) usando lenguaje abusivo 

O comportamiento imprudente, bochinchear, causar trastornos o molestar a otros 

Estudiantes, que son hábitos detestables. Acordamos con cada estudiante abstenerse 

De ellos.  <br/>

7. Vestido no islámico y vestimenta que no sea acorde a las reglas del centro educativo 

Serán confiscadas. Vestimenta con logo o impresión no será permitido. No se tolerará 

La contradicción de los principios del centro educativo.  <br/>

8. Ausencia no autorizada de clase será tratada en forma seria. Si se da el caso de algún 

Asunto urgente que requiera atención inmediata, los estudiantes internos deberán 

Requerir un pase de salida con una nota escrita por ellos mismos en la direccion. 

Peticiones enviadas a través de otros estudiantes no seran aceptadas. Similarmente 

Peticiones para salidas por razones de enfermedad solo se otorgaran cuando la 

Asistencia se vuelva imposible o dañina a la condición de salud del estudiante. El 

Permiso de salida a cualquier sitio por cualquier necesidad deberá ser obtenido de la 

Direccion. Permiso de salida el sábado en la tarde hasta el domingo en la tarde no será 

Otorgado sin petición formal.  <br/>

9. Estudiantes que no regresen a tiempo después de habérseles otorgado un permiso, y 

Estudiantes que lleguen tarde después de días de vacaciones, serán sujetos a la decisión 

Del shura quien decidirá de acuerdo al individuo. 

10. Es obligación de cada estudiante proteger y cuidar la propiedad y el equipo del centro 

Educativo. <br/>

11. Se requerirá que los estudiantes acepten la decisión del shura en cuanto a sus dias 

Feriados y como son utilizados (tarawih, jamat, etc.). <br/>

12. Todos los mayores deberán ser respetados en particular el ustad y el maestro, no es 

Necesario que el estudiante este con ese ustad o no, y con todos los relacionados al 

Centro educativo. <br/>

13. No se responsabilizara al centro educativo si el estudiante deja las premisas sin permiso 

O va a otro lugar que no sea para el que se le haya otorgado el permiso. <br/>

14. Todas las quejas deberán ser referidas al shura, ningun guardián/pariente o niño 

Será permitido tomar la ley en sus propias manos. Ningún padre tiene el derecho de 

Confrontar directamente a ningún ustad. <br/>

15. Los estudiantes que no lleguen a cumplir con el estándar de educacion del centro 

Educativo estarán sujeto a la decisión del shura. Los estudiantes que fracasen en tres 

Materias se le requerirá que presenten el examen de nuevo en shawwal, después del cual 

Serán sujetos a cualquier decisión que el shura decida. <br/>

16. Los padres/guardián deberán asistir al ser requeridos. En cuanto al bienestar espiritual 

De su hijo, los padres se les sugiere que deben pasar tiempo en jamat con ellos después 

De los examenes anuales. Todas las quejas con respecto al centro educativo deberán 

Ser enviadas al shura del madresah. Los ustads o maestros no deberán ser contactados 

Individualmente a menos que haya sido aceptado por el shura. <br/>

17. Los estudiantes en internado deberán declarar cualquier plan de matrimonio al shura. 

Después de aceptar cualquier tal petición los estudiantes deberán residir en las premisas 

Del madresah, en cuanto al mashuerah, la salida a casa será otorgada una vez cada 

Dos semanas. <br/>

18. Cualquier peticion de salida para matrimonios, umrah o cualquier otro evento será 

Decidido en vista del mejor interés tanto del instituto como del estudiante en questión. 

Los padres/guardianes y estudiantes deberán aceptar gustosos la decisión. <br/>

19. Cualquier actividad que pueda afectar los estudios tales como trabajo, manejar 

Negocio queda estrictamente prohibido durante el tiempo de estudio. Para estudiantes 

Que sufren problemas financieros, ayuda y consejo deberá buscarse con el shura. <br/>

20. La condición más importante para obtener conocimiento islamico es confiar 

Siceramente, honrar y respetar a los maestros y miembros del personal del instituto. <br/>

21. Estudiantes con el habito de ambular fuera del madresah, socializar con extraños y 

Entretener visitas innecesarias serán expulsados si no cambia despues de haber sido 

Amonestado. Ninguna visita podrá ser acomodada en el dormitorio del estudiante. <br/>

22. Se espera que todos los estudiantes mantengan sus cuartos y corredores limpios y 

Ordenados y que se desagan de cualquier material innecesario en el lugar de rezar y 

No causar ningún daño a los salones, dormitorios y corredores o escribir grafitis en 

Las paredes, baños o pupitres. El área de lavado deberá mantenerse limpio después de 

Lavar ropa y utencilios etc. Los estudiantes deberán ser ejemplos de limpieza, orden, alta 

Moral y devota. <br/>

23. Cualquier artículo que no se permita en el madresah será confiscado y retirado, 

Ejemplo: grabadoras, casetes, celulares y vestuarios no islámico, etc. Lo confiscado no 

Se devolverá. <br/>

24. El pago se realizara en tres abonos, el primero siendo entregado al final de shawwal, el 

Segundo para el 15 de safar y el tercero para el 15 de jamad ul ukhra. El shura no podrá 

Tener ninguna consideración después de estas fechas. Los padres/guardianes tendrán 

Que aceptar las consecuensias si no pagan en estas fechas. <br/>

25. La anualidad será de $ 2,250.00 dolares + $ 150.00 dolares de matricula. Los padres que 

Quieran ahorrarse tiempo firmando una orden de admision seran binvenidos. Los 

Formularios se pueden obtener en la oficina. <br/>

26. El curso de árabe empieza en shawwal. <br/>

27. A ningún estudiante se le permitira guardar dinero para sus gastos que sean de otros 

Estudiantes. La oficina proveerá el servicio. <br/>

28. El personal del madresah, shura y maestros de escuela desean lo mejor para los 

Estudiantes y en vista de su experiencia desean lo mejor para ellos. Ya sea en materia 

Referente a educación, arreglos para taraweeh o jamat. El instituto siempre decidirá 

En el mejor interés del estudiante, por lo tanto actuar bajo su guiá será inshallah, para 

Hacer que sus vidas tengan un proposito de éxito. Por tal razón, esperamos que una 

Vez sea tomada la decisión de enviar al estudiante en el curso de alim o hafiz ninguna 

Alteracion sea hecha durante el curso del año. <br/>

29. Ropa y otros artículos que no sean reclamados seran entregados a caridad. <br/>

30. El madresah puede arreglar, quitar o alterar las reglas sin previa notificación. <br/>

31. El estudiante podrá ser expulsado si no obedece las reglas del madresah o si es la 

Causa de algún problema, que por opinion del shura cause algun daño al madresah, su 

Personal o algún estudiante. <br/>

32. Se le requiere a los estudiantes usar uniforme blanco. Mayor explicacion se le dará en 

Tiempo de entrevista. <br/>
